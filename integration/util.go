package integration

import (
	gsl "github.com/lucky-se7en/ggsl"
	"math"
)

func subinterval_too_small(a1, a2, b2 float64) bool {
	tmp := (1 + 100*gsl.Float64Eps) * (math.Abs(a2) + 1000*gsl.MinFloat64)
	return math.Abs(a1) <= tmp && math.Abs(b2) <= tmp
}
